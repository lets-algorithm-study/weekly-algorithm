function solution(progresses, speeds) {
  const result = [];
  const days = progresses.map((progress, index) =>
    // 100 - í˜„ì¬ ì§„í–‰ë„ / ì†ë„ êµ¬í•˜ê³  ì˜¬ë¦¼ì²˜ë¦¬.
    Math.ceil((100 - progress) / speeds[index])
  );
  let max = days[0];
  let count = 1;

  for (let i = 1; i < days.length; i++) {
    if (days[i] <= max) {
      count++;
    } else {
      result.push(count);
      max = days[i];
      count = 1;
    }
  }

  result.push(count);

  return result;
}

